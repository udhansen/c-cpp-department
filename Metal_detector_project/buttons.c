void but_setup()
{
	DDRE &=~ (1<<PE4) | (1<<PE5) | (1<<PE3); // sets these to 1 as outputs 

	PORTE |=(1<<PE4) | (1<<PE5) | (1<<PE3); // sets these 3 outputs as high
	
	//setup for inputs
	DDRA &=~ (1<<PA1) | (1<<PA3) | (1<<PA5);
	PORTA = (1<<PA1) | (1<<PA3) | (1<<PA5);
	
}

int but_clicks() 
// registrere om en knap er trykket og sætter den tilsvarende værdi i variablen som retuneres
{

	int click_state = 0;

	if(!(PINA & (1<<PA1))) // start og stop
	{
		
		
		setXY(1,3);
		sendStr("Running     ");
		click_state = 1;
		
	}
	
	
	else if(!(PINA & (1<<PA3))) // hard reset
	{
		
		setXY(1,3);
		sendStr("Resetting...  ");
		setXY(1,3);
		sendStr("Running      ");
		click_state = 2;
	}
	else if(!(PINA & (1<<PA5))) //offset
	{
		setXY(1,3);
		sendStr("Offset... ");
		click_state = 3;
		_delay_ms(250);

	}
	else {click_state = 0;}
	
	return click_state;
	

}

